import sys
from collections import deque

input = sys.stdin.readline

r, c = map(int, input().split())
arr = [list(input().strip()) for x in range(r)]


def escape(arr):
    dx = [1, -1, 0, 0]
    dy = [0, 0, 1, -1]
    t = 1

    que = deque([])
    for i in range(r):
        for j in range(c):
            if arr[i][j] == "*":
                que.append([i, j])
            elif arr[i][j] == "S":
                start = [t, i, j]
                arr[i][j] = "."

    # 시간이 얼마나 지나야 물이 차는지 모든 칸에 표시
    while que:
        x, y = que.popleft()

        for i in range(4):
            nx = x + dx[i]
            ny = y + dy[i]

            if 0 <= nx < r and 0 <= ny < c and arr[nx][ny] == ".":
                que.append([nx, ny])

                if arr[x][y] == "*":
                    arr[nx][ny] = 1
                else:
                    arr[nx][ny] = arr[x][y] + 1

    # 길찾기
    que.append(start)
    while que:
        t, x, y = que.popleft()

        for i in range(4):
            nx = x + dx[i]
            ny = y + dy[i]

            if 0 <= nx < r and 0 <= ny < c:
                # 현재 시간이 물이 차기 전일 때 or 물이 차지 않는 곳이면 전진
                if (type(arr[nx][ny]) == int and arr[nx][ny] > t) or arr[nx][ny] == ".":
                    que.append([t + 1, nx, ny])
                    arr[nx][ny] = "X"
                elif arr[nx][ny] == "D":
                    return t

    return "KAKTUS"


print(escape(arr))

